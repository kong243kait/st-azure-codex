    /**
     * Make an element draggable with mouse & touch support.
     * Saves position to localStorage. Distinguishes drag vs click.
     * @param {HTMLElement} el - The element to make draggable
     * @param {string} storageKey - localStorage key to persist position
     * @param {boolean} isFloatBtn - Whether this is the floating button (uses bottom/right)
     */
    _makeDraggable(el, storageKey, isFloatBtn = false) {
        let isDragging = false;
        let hasMoved = false;
        let startX = 0, startY = 0;
        let elStartX = 0, elStartY = 0;
        const DRAG_THRESHOLD = 8;

        const isMobile = () => window.innerWidth <= 768;

        // Restore saved position (works on all viewports since we use JS positioning)
        try {
            const saved = JSON.parse(localStorage.getItem(storageKey) || 'null');
            if (saved && typeof saved.x === 'number' && typeof saved.y === 'number') {
                requestAnimationFrame(() => {
                    const w = el.offsetWidth || 140;
                    const h = el.offsetHeight || 44;
                    const vh = window.innerHeight;
                    const vw = window.innerWidth;
                    const clamped = this._clampToViewport(saved.x, saved.y, w, h);
                    // Validate: discard stale positions from the old broken positioning era
                    // If stored position was negative, near top edge, or way off-screen, ignore it
                    if (saved.y < 0 || saved.y > vh || saved.x < -w || saved.x > vw) {
                        console.debug(LOG_PREFIX, 'Discarding stale stored position:', saved);
                        localStorage.removeItem(storageKey);
                        return; // Let _positionFloatBtn handle fresh positioning
                    }
                    el.style.left = clamped.x + 'px';
                    el.style.top = clamped.y + 'px';
                    el.style.right = 'auto';
                    el.style.bottom = 'auto';
                    if (isFloatBtn) {
                        el.style.transform = 'none';
                        el.dataset.dragged = 'true';
                    }
                });
            }
        } catch { /* ignore */ }

        const getPos = (e) => {
            if (e.touches && e.touches.length > 0) return { x: e.touches[0].clientX, y: e.touches[0].clientY };
            return { x: e.clientX, y: e.clientY };
        };

        const onStart = (e) => {
            // For the float button itself, allow drag on the button
            if (!isFloatBtn) {
                const tag = /** @type {HTMLElement} */ (e.target).tagName;
                if (['BUTTON', 'INPUT', 'SELECT', 'TEXTAREA', 'A'].includes(tag)) return;
            }

            isDragging = true;
            hasMoved = false;
            const pos = getPos(e);
            startX = pos.x;
            startY = pos.y;

            const rect = el.getBoundingClientRect();
            elStartX = rect.left;
            elStartY = rect.top;

            el.style.transition = 'none';
            el.style.animation = 'none';
        };

        const onMove = (e) => {
            if (!isDragging) return;
            const pos = getPos(e);
            const dx = pos.x - startX;
            const dy = pos.y - startY;

            if (!hasMoved && Math.abs(dx) < DRAG_THRESHOLD && Math.abs(dy) < DRAG_THRESHOLD) return;
            hasMoved = true;
            e.preventDefault();

            const newX = elStartX + dx;
            const newY = elStartY + dy;
            const clamped = this._clampToViewport(newX, newY, el.offsetWidth, el.offsetHeight);

            el.style.left = clamped.x + 'px';
            el.style.top = clamped.y + 'px';
            el.style.right = 'auto';
            el.style.bottom = 'auto';
            if (isFloatBtn) el.style.transform = 'none';
        };

        const onEnd = () => {
            if (!isDragging) return;
            isDragging = false;
            el.style.transition = '';

            if (hasMoved) {
                if (isFloatBtn) el.dataset.dragged = 'true';
                const rect = el.getBoundingClientRect();
                try {
                    localStorage.setItem(storageKey, JSON.stringify({ x: rect.left, y: rect.top }));
                } catch { /* quota full */ }
            }
        };

        // Mouse events
        el.addEventListener('mousedown', onStart);
        document.addEventListener('mousemove', onMove, { signal: this._dragAbortController?.signal });
        document.addEventListener('mouseup', onEnd, { signal: this._dragAbortController?.signal });

        // Touch events
        el.addEventListener('touchstart', onStart, { passive: true });
        document.addEventListener('touchmove', onMove, { passive: false, signal: this._dragAbortController?.signal });
        document.addEventListener('touchend', onEnd, { signal: this._dragAbortController?.signal });

        // Override click to prevent toggle when dragged
        if (isFloatBtn) {
            el.addEventListener('click', (e) => {
                if (hasMoved) {
                    e.stopImmediatePropagation();
                    e.preventDefault();
                    hasMoved = false;
                    return false;
                }
            }, true);
        }
    }

    /**
     * Make the overlay panel draggable by its header (desktop only).
     * On mobile (≤768px), panel is full-width bottom sheet — no dragging.
     */
    _makePanelDraggable(panel, handle) {
        let isDragging = false;
        let hasMoved = false;
        let startX = 0, startY = 0;
        let elStartX = 0, elStartY = 0;
        const DRAG_THRESHOLD = 5;

        const isMobile = () => window.innerWidth <= 768;

        const onStart = (e) => {
            if (isMobile()) return; // Never drag panel on mobile
            const tag = /** @type {HTMLElement} */ (e.target).tagName;
            if (['BUTTON', 'INPUT', 'SELECT', 'TEXTAREA', 'A'].includes(tag)) return;
            if (/** @type {HTMLElement} */ (e.target).closest('.ac-toggle-wrap, .ac-close-btn, .ac-toggle')) return;

            isDragging = true;
            hasMoved = false;
            const pos = e.touches ? { x: e.touches[0].clientX, y: e.touches[0].clientY } : { x: e.clientX, y: e.clientY };
            startX = pos.x;
            startY = pos.y;
            const rect = panel.getBoundingClientRect();
            elStartX = rect.left;
            elStartY = rect.top;
            panel.style.transition = 'none';
        };

        const onMove = (e) => {
            if (!isDragging || isMobile()) return;
            const pos = e.touches ? { x: e.touches[0].clientX, y: e.touches[0].clientY } : { x: e.clientX, y: e.clientY };
            const dx = pos.x - startX;
            const dy = pos.y - startY;
            if (!hasMoved && Math.abs(dx) < DRAG_THRESHOLD && Math.abs(dy) < DRAG_THRESHOLD) return;
            hasMoved = true;
            e.preventDefault();
            const newX = elStartX + dx;
            const newY = elStartY + dy;
            const clamped = this._clampToViewport(newX, newY, panel.offsetWidth, panel.offsetHeight);
            panel.style.left = clamped.x + 'px';
            panel.style.top = clamped.y + 'px';
            panel.style.right = 'auto';
            panel.style.bottom = 'auto';
            panel.style.transform = 'none';
        };

        const onEnd = () => {
            if (!isDragging) return;
            isDragging = false;
            panel.style.transition = '';
        };

        handle.addEventListener('mousedown', onStart);
        document.addEventListener('mousemove', onMove, { signal: this._dragAbortController?.signal });
        document.addEventListener('mouseup', onEnd, { signal: this._dragAbortController?.signal });
        handle.addEventListener('touchstart', onStart, { passive: true });
        document.addEventListener('touchmove', onMove, { passive: false, signal: this._dragAbortController?.signal });
        document.addEventListener('touchend', onEnd, { signal: this._dragAbortController?.signal });
    }

    /** Clamp coordinates so the element stays within the visible viewport */
    _clampToViewport(x, y, w, h) {
        const vw = window.innerWidth;
        const vh = window.innerHeight;
        return {
            x: Math.max(0, Math.min(x, vw - w)),
            y: Math.max(0, Math.min(y, vh - h)),
        };
    }

